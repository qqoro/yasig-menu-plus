// 현재 정규식
const currentRegex = /[RBV]J\d{6,8}/i;

// 개선된 정규식 (대괄호 우선)
function getImprovedId(path: string): string | undefined {
  // 1순위: 대괄호 안의 RJ 코드 추출
  const bracketMatch = /\[([RBV]J\d{6,8})\]/i.exec(path);
  if (bracketMatch) {
    return bracketMatch[1];
  }
  // 2순위: 대괄호 없는 RJ 코드
  return currentRegex.exec(path)?.[0];
}

